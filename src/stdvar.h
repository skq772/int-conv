// Abbreviations for basic variables for C and C++ language
// Written for greater clarity on variable size
// It's the same as rust language variables
#ifndef _C_STDVAR_H_
#define _C_STDVAR_H_
#define i8 signed char
#define u8 unsigned char
#define i16 signed short int
#define u16 unsigned short int
#define i32 signed int
#define u32 unsigned int
#define i64 signed long long int
#define u64 unsigned long long int
#define f32 float
#define f64 double
#endif
